void Write_Command(uint8_t command , uint8_t value)
{
	PacketAddress[0] = packetaddress;
	Command[0] = command;
	Value[0] = value;
	HAL_UART_Transmit(&huart1 , (uint8_t*)PacketAddress , 1 , 2000);
	HAL_UART_Transmit(&huart1 , (uint8_t*)Command , 1 ,2000);
	HAL_UART_Transmit(&huart1 , (uint8_t*)Value , 1 , 2000);
	AllWrite[0] = ((packetaddress + command + value) & 127);
	HAL_UART_Transmit(&huart1 , (uint8_t*)AllWrite , 1 , 2000);
}
