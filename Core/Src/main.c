void SetBaudRate(uint16_t BaudRate)
{
	  uint8_t value;
	  switch(BaudRate)
		{
			case 9600 : value = 2 ;
									break;
			case 19200: default : value = 3;
									break;
		}

		HAL_Delay(500);
		Write_Command(15 , value);
		HAL_Delay(2000);
}


